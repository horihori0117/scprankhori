# SCP計算（キャッシュ非使用）
def calculate_cp(AS, BS, CS, AK, BK, CK, CPM):
    return math.floor((AS + AK) * math.sqrt(BS + BK) * math.sqrt(CS + CK) * (CPM ** 2) / 10)

def calculate_scp(AS, BS, CS, AK, BK, CK, CPM):
    hp_term = math.floor((CS + CK) * CPM)
    val = ((((AS + AK) * CPM) * ((BS + BK) * CPM) * hp_term) ** (2 / 3)) / 10
    return val

