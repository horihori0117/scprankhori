def load_stats_from_csv(filename="stats.csv"):
    stats = {}
    order = []
    display_map = {}

    with open(filename, encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            name = row["名前"]
            form = row["形態"]
            key = (name, form)
            AS = float(row["AS"])
            BS = float(row["BS"])
            CS = float(row["CS"])
            stats[key] = (AS, BS, CS)
            order.append(key)

            if "表示" in row and row["表示"]:
                related = row["表示"].split(",")
                parsed_related = []
                for entry in related:
                    entry = entry.strip()
                    if "(" in entry and ")" in entry:
                        n = entry[:entry.index("(")]
                        f = entry[entry.index("(")+1:entry.index(")")].strip()
                    else:
                        n, f = entry.strip(), ""
                    parsed_related.append((n, f))
                display_map[key] = parsed_related

    order.sort(key=lambda x: x[0])
    return stats, order, display_map

def load_cpm_from_csv(filename="cpm.csv"):
    cpm_list = []
    cpm_to_level = {}
    level_to_cpm = {}

    with open(filename, encoding='utf-8') as f:
        reader = csv.DictReader(f)
        for row in reader:
            level = float(row["Level"])
            cpm = float(row["CPM"])
            cpm_list.append(cpm)
            cpm_to_level[round(cpm, 10)] = level
            level_to_cpm[level] = cpm

    return cpm_list, cpm_to_level, level_to_cpm

