import numpy as np

def get_scp_rankings_numpy(AS, BS, CS, cpm_list, cp_limit):
    iv_range = np.arange(16)
    AK, BK, CK = np.meshgrid(iv_range, iv_range, iv_range, indexing='ij')
    AK = AK.flatten()
    BK = BK.flatten()
    CK = CK.flatten()
    n = len(AK)

    AS_arr = np.full(n, AS)
    BS_arr = np.full(n, BS)
    CS_arr = np.full(n, CS)

    best_scp = np.zeros(n)
    best_cp = np.zeros(n)
    best_cpm = np.zeros(n)

    for CPM in cpm_list:
        atk = (AS_arr + AK) * CPM
        dfs = (BS_arr + BK) * CPM
        hp_term = np.floor((CS_arr + CK) * CPM)
        scp = (((atk * dfs * hp_term) ** (2/3)) / 10)

        cp = np.floor((AS_arr + AK) * np.sqrt(BS_arr + BK) * np.sqrt(CS_arr + CK) * (CPM ** 2) / 10)

        # CP 制限フィルタ
        valid = True if cp_limit is None else cp <= cp_limit

        update = (scp > best_scp) & valid
        best_scp = np.where(update, scp, best_scp)
        best_cp = np.where(update, cp, best_cp)
        best_cpm = np.where(update, CPM, best_cpm)

    result = []
    for i in range(n):
        if best_scp[i] > 0:
            result.append({
                "AK": int(AK[i]), "BK": int(BK[i]), "CK": int(CK[i]),
                "SCP": float(best_scp[i]),
                "CP": int(best_cp[i]),
                "CPM": float(best_cpm[i])
            })

    # タイ順位処理
    result.sort(key=lambda x: x["SCP"], reverse=True)
    current_rank = 1
    last_scp = None
    for i, r in enumerate(result):
        if last_scp is None or r["SCP"] != last_scp:
            current_rank = i + 1
        r["rank"] = current_rank
        last_scp = r["SCP"]

    return result

def get_competitive_rank(rankings, AK, BK, CK):
    for r in rankings:
        if r["AK"] == AK and r["BK"] == BK and r["CK"] == CK:
            return r["rank"]
    return None

